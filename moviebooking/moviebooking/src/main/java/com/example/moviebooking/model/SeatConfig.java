package com.example.moviebooking.model;

public class SeatConfig {
    private String row;
    private int seats;

    // Getters and Setters
    public String getRow() {
        return row;
    }

    public void setRow(String row) {
        this.row = row;
    }

    public int getSeats() {
        return seats;
    }

    public void setSeats(int seats) {
        this.seats = seats;
    }
}
